public class DiscordEventHandler implements EventListener {
	@Override
	public void onEvent(GenericEvent event) {
		// Guild
		if (event instanceof GuildReadyEvent) {
			Guilds.addGuild(((GuildReadyEvent) event).getGuild());
		}
	}
}
